#include <iostream> 	// header allows for console input and output
using namespace std;	// uses the standard C++ namespace (avoids std::) 

int main()
{
	cout << 17 << endl; 		// int literal
	cout << 1234.56 << endl;	// float / double literal
	cout << 'a' << endl;		// char literal
	cout << "THIS IS A STRING" << endl; // String literal
	cout << true; 				// boolean literal
}
